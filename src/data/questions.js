export function getIndicesPorMundo(mundoId) {
  const cats = worldCategoryMap[mundoId];
  if (!cats) return null;
  const indices = [];
  for (const cat of cats) {
    const catIdx = categoryIndices[cat];
    if (catIdx) indices.push(...catIdx);
  }
  return indices.length > 0 ? indices : null;
}

