export const categoryIndices = {};
(function buildCategoryIndices() {
  let idx = 0;
  for (const cat of Object.keys(bancoQuestoes)) {
    categoryIndices[cat] = [];
    for (const _ of bancoQuestoes[cat]) {
      categoryIndices[cat].push(idx++);
    }
  }
})();

